package If_min_max;

public class What_If_Min_and_Max {

	public static void main(String[] args) {
		
		int a = 3;
		int b = 8;
		int c = 5;
		
		int min = 0; 
		int max = 10;
		
		
		if (a < c && b > a);
			min = c;
			max = b; 
		
		System.out.println("Min and max values:");
		System.out.println("min =" + min);
		System.out.println("max =" + max);
		
		

	}
	
	
}
